age = 15  # 赋值运算符

age += 1  # 加法赋值运算符(复合)

# 减法复合运算符
age1 = 15
age1 -= 1
print(age1)

# 乘法赋值运算符
count = 0.5
sum = 100
sum *= count
print(sum)

# 除法复合运算符
pay = 100
num1 = 5
pay /= num1
print(pay)

# 取整赋值运算符
apple = 31
num2 = 14
apple //= num2
print(apple)

# 取模赋值运算符
ok = 10
num3 = 3
ok %= num3
print(ok)

# 指数赋值运算
a = 2
b = 3
a **= b
print(a)
